def build_graph_for_group(edges_df, selected_terms, group_name):
    """
    Build BP correlation graph for a group.
    Nodes = selected BP terms.
    Edges = V3 correlation edges for that group.
    """
    G = nx.Graph()

    selected_terms = [str(x) for x in selected_terms if pd.notna(x)]
    G.add_nodes_from(selected_terms)

    if edges_df.empty:
        return G

    sub = edges_df[edges_df["group"].astype(str) == group_name].copy()

    if sub.empty:
        return G

    for _, r in sub.iterrows():
        a = str(r["bp1"])
        b = str(r["bp2"])

        if a not in G.nodes or b not in G.nodes:
            continue

        w = safe_float(r.get(EDGE_WEIGHT_COL, np.nan), default=np.nan)
        corr = safe_float(r.get("corr", np.nan), default=np.nan)
        p_corr = safe_float(r.get("p_corr", np.nan), default=np.nan)

        if pd.isna(w):
            w = abs(corr) if not pd.isna(corr) else 1.0

        G.add_edge(a, b, weight=w, corr=corr, p_corr=p_corr)

    return G


def detect_communities(G, method="greedy_modularity"):
    """
    Return dict node -> module_id and list of communities.
    """
    nodes = list(G.nodes())

    if len(nodes) == 0:
        return {}, []

    if G.number_of_edges() == 0:
        if SINGLETON_IF_NO_EDGES:
            communities = [{n} for n in nodes]
        else:
            communities = [set(nodes)]
    else:
        if method == "greedy_modularity":
            communities = list(nx.algorithms.community.greedy_modularity_communities(G, weight="weight"))
            communities = [set(c) for c in communities]
        else:
            raise ValueError(f"Unknown community method: {method}")

    # Sort modules by size descending, then name
    communities = sorted(communities, key=lambda c: (-len(c), sorted(list(c))[0] if c else ""))

    node_to_module = {}
    for i, comm in enumerate(communities, start=1):
        module_id = f"M{i}"
        for n in comm:
            node_to_module[n] = module_id

    return node_to_module, communities


