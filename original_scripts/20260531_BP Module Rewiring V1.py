def plot_module_graph(G, assignment_df, cancer_code, group_name, out_path):
    """
    Network colored by module.
    Uses matplotlib default color cycle.
    """
    if G.number_of_nodes() == 0:
        return

    module_map = {}
    if not assignment_df.empty:
        module_map = assignment_df.set_index("term")["module_id"].to_dict()

    modules = sorted(set(module_map.get(n, "M?") for n in G.nodes()))
    mod_to_idx = {m: i for i, m in enumerate(modules)}
    node_colors = [mod_to_idx.get(module_map.get(n, "M?"), 0) for n in G.nodes()]

    degrees = dict(G.degree())
    node_sizes = [100 + 40 * degrees.get(n, 0) for n in G.nodes()]

    if G.number_of_edges() > 0:
        weights = [G[u][v].get("weight", 0.5) for u, v in G.edges()]
        widths = [0.5 + 2.0 * w for w in weights]
    else:
        widths = []

    plt.figure(figsize=(11, 9))
    pos = nx.spring_layout(G, seed=3, k=0.9)

    nx.draw_networkx_nodes(
        G,
        pos,
        node_size=node_sizes,
        node_color=node_colors,
        alpha=0.85,
        cmap=plt.cm.tab20
    )

    if G.number_of_edges() > 0:
        nx.draw_networkx_edges(
            G,
            pos,
            width=widths,
            alpha=0.35
        )

    nx.draw_networkx_labels(
        G,
        pos,
        labels={n: shorten_label(n) for n in G.nodes()},
        font_size=7
    )

    plt.title(f"{cancer_code}: {group_name} BP modules")
    plt.axis("off")
    plt.tight_layout()
    plt.savefig(out_path, dpi=300)
    plt.close()


def plot_module_size_bar(module_summary_df, cancer_code, out_path):
    if module_summary_df.empty:
        return

    plot_df = module_summary_df.copy()
    plot_df["module_label"] = plot_df["group"].astype(str) + ":" + plot_df["module_id"].astype(str)

    plt.figure(figsize=(12, 6))
    x = np.arange(plot_df.shape[0])
    plt.bar(x, plot_df["n_BP"].values)
    plt.xticks(x, plot_df["module_label"].tolist(), rotation=90, fontsize=7)
    plt.ylabel("Number of BP in module")
    plt.title(f"{cancer_code}: module sizes")
    plt.tight_layout()
    plt.savefig(out_path, dpi=300)
    plt.close()


def plot_overlap_heatmap(overlap_df, cancer_code, out_path):
    if overlap_df.empty:
        return

    early_modules = sorted(overlap_df["early_module"].unique())
    late_modules = sorted(overlap_df["late_module"].unique())

    mat = pd.DataFrame(0.0, index=early_modules, columns=late_modules)

    for _, r in overlap_df.iterrows():
        mat.loc[r["early_module"], r["late_module"]] = r["jaccard_BP"]

    plt.figure(figsize=(max(6, len(late_modules) * 0.8), max(5, len(early_modules) * 0.8)))
    plt.imshow(mat.values, aspect="auto", interpolation="nearest")
    plt.colorbar(label="BP Jaccard overlap")

    plt.xticks(range(len(late_modules)), late_modules, rotation=45, ha="right")
    plt.yticks(range(len(early_modules)), early_modules)

    plt.xlabel("Late modules")
    plt.ylabel("Early modules")
    plt.title(f"{cancer_code}: Early vs Late module overlap")

    plt.tight_layout()
    plt.savefig(out_path, dpi=300)
    plt.close()


