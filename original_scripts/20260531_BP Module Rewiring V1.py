def run_module_rewiring_one_cancer(cancer_dir):
    cancer_dir = Path(cancer_dir)
    data = load_v3_cancer_outputs(cancer_dir)
    cancer_code = data["cancer_code"]

    print("\n" + "=" * 100)
    print(f"[Cancer] {cancer_code}")
    print(f"[Input dir] {cancer_dir}")

    dstage = data["dstage"]
    selected = data["selected"]
    edges = data["edges"]
    node_metrics = data["node_metrics"]
    net_summary = data["net_summary"]
    gene_overlap = data["gene_overlap"]

    if selected.empty:
        print("[Skip] selected BP file empty.")
        return None

    if "term" not in selected.columns:
        print("[Skip] selected BP file has no term column.")
        return None

    selected_terms = selected["term"].dropna().astype(str).tolist()

    if len(selected_terms) == 0:
        print("[Skip] no selected BP terms.")
        return None

    cancer_out = OUT_DIR / cancer_code
    cancer_out.mkdir(parents=True, exist_ok=True)

    all_assignments = []
    all_module_summaries = []

    graph_by_group = {}
    assignment_by_group = {}

    for group_name in NETWORK_GROUPS:
        G = build_graph_for_group(edges, selected_terms, group_name)
        graph_by_group[group_name] = G

        node_to_module, communities = detect_communities(G, method=COMMUNITY_METHOD)

        assignment_df = compute_module_assignment(
            cancer_code=cancer_code,
            group_name=group_name,
            G=G,
            node_to_module=node_to_module,
            dstage_df=dstage,
            node_metrics_df=node_metrics
        )

        assignment_by_group[group_name] = assignment_df
        all_assignments.append(assignment_df)

        module_summary_df = compute_module_summary(
            cancer_code=cancer_code,
            group_name=group_name,
            G=G,
            module_assignment_df=assignment_df,
            gene_overlap_df=gene_overlap
        )

        all_module_summaries.append(module_summary_df)

        print(f"[{group_name}] nodes={G.number_of_nodes()} edges={G.number_of_edges()} modules={module_summary_df.shape[0]}")

        if MAKE_FIGURES:
            plot_module_graph(
                G,
                assignment_df,
                cancer_code,
                group_name,
                cancer_out / f"{cancer_code}_module_graph_{group_name}.png"
            )

    assignment_all = pd.concat(all_assignments, ignore_index=True) if all_assignments else pd.DataFrame()
    module_summary_all = pd.concat(all_module_summaries, ignore_index=True) if all_module_summaries else pd.DataFrame()

    assignment_all.to_csv(cancer_out / f"{cancer_code}_module_assignment.csv", index=False)
    module_summary_all.to_csv(cancer_out / f"{cancer_code}_module_summary.csv", index=False)

    # Early vs Late module rewiring
    assign_early = assignment_by_group.get(EARLY_GROUP, pd.DataFrame())
    assign_late = assignment_by_group.get(LATE_GROUP, pd.DataFrame())

    overlap_df, rewiring_df = compute_module_overlap_matrix(assign_early, assign_late, cancer_code)

    overlap_df.to_csv(cancer_out / f"{cancer_code}_module_overlap_matrix_early_vs_late.csv", index=False)
    rewiring_df.to_csv(cancer_out / f"{cancer_code}_module_rewiring_early_vs_late.csv", index=False)

    if MAKE_FIGURES:
        plot_module_size_bar(
            module_summary_all,
            cancer_code,
            cancer_out / f"{cancer_code}_module_sizes.png"
        )
        plot_overlap_heatmap(
            overlap_df,
            cancer_code,
            cancer_out / f"{cancer_code}_module_overlap_heatmap_early_vs_late.png"
        )

    summary = summarize_rewiring(
        cancer_code=cancer_code,
        rewiring_df=rewiring_df,
        module_summary_df=module_summary_all,
        net_summary_df=net_summary
    )

    with open(cancer_out / f"{cancer_code}_module_rewiring_summary.json", "w", encoding="utf-8") as f:
        json.dump(summary, f, indent=2)

    print("[Summary]")
    for k, v in summary.items():
        print(f"  {k}: {v}")

    return {
        "summary": summary,
        "assignment": assignment_all,
        "module_summary": module_summary_all,
        "rewiring": rewiring_df,
        "overlap": overlap_df,
    }


