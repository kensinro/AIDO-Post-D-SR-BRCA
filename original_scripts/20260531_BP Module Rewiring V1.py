def compute_module_assignment(cancer_code, group_name, G, node_to_module, dstage_df, node_metrics_df):
    """
    BP-level module assignment table.
    """
    rows = []

    dstage_map = {}
    if not dstage_df.empty and "term" in dstage_df.columns:
        tmp = dstage_df.set_index("term", drop=False)
        dstage_map = tmp.to_dict(orient="index")

    metric_sub = pd.DataFrame()
    if not node_metrics_df.empty and "group" in node_metrics_df.columns:
        metric_sub = node_metrics_df[node_metrics_df["group"].astype(str) == group_name].copy()

    metric_map = {}
    if not metric_sub.empty and "term" in metric_sub.columns:
        metric_map = metric_sub.set_index("term", drop=False).to_dict(orient="index")

    for node in G.nodes():
        module_id = node_to_module.get(node, "M_unassigned")
        drow = dstage_map.get(node, {})
        mrow = metric_map.get(node, {})

        rows.append({
            "cancer": cancer_code,
            "group": group_name,
            "term": node,
            "module_id": module_id,
            "degree": safe_float(mrow.get("degree", G.degree(node)), default=G.degree(node)),
            "weighted_degree": safe_float(mrow.get("weighted_degree", G.degree(node, weight="weight")), default=G.degree(node, weight="weight")),
            "betweenness": safe_float(mrow.get("betweenness", np.nan), default=np.nan),
            "D_stage": safe_float(drow.get("D_stage", np.nan)),
            "p_stage": safe_float(drow.get("p_stage", np.nan)),
            "delta_late_minus_early": safe_float(drow.get("delta_late_minus_early", np.nan)),
            "stage_trend": drow.get("stage_trend", None),
            "mu_early": safe_float(drow.get("mu_early", np.nan)),
            "mu_late": safe_float(drow.get("mu_late", np.nan)),
            "n_early": safe_float(drow.get("n_early", np.nan)),
            "n_late": safe_float(drow.get("n_late", np.nan)),
        })

    out = pd.DataFrame(rows)

    if not out.empty:
        out = out.sort_values(
            ["group", "module_id", "degree", "weighted_degree", "D_stage"],
            ascending=[True, True, False, False, False]
        )

    return out


def module_internal_edge_stats(G, nodes):
    """
    Compute internal graph stats for a module.
    """
    nodes = list(nodes)
    H = G.subgraph(nodes).copy()

    n_nodes = H.number_of_nodes()
    n_edges = H.number_of_edges()
    density = nx.density(H) if n_nodes > 1 else 0.0

    if n_edges > 0:
        weights = [d.get("weight", 1.0) for _, _, d in H.edges(data=True)]
        mean_weight = float(np.mean(weights))
        max_weight = float(np.max(weights))
    else:
        mean_weight = 0.0
        max_weight = 0.0

    return {
        "module_n_nodes": int(n_nodes),
        "module_n_edges": int(n_edges),
        "module_density": float(density),
        "module_mean_edge_weight": mean_weight,
        "module_max_edge_weight": max_weight,
    }


def compute_module_gene_overlap_stats(module_terms, gene_overlap_df):
    """
    Use pairwise BP gene overlap table to estimate redundancy inside a module.
    """
    terms = set(module_terms)

    if gene_overlap_df.empty or len(terms) <= 1:
        return {
            "mean_jaccard": np.nan,
            "max_jaccard": np.nan,
            "mean_overlap_coefficient": np.nan,
            "max_overlap_coefficient": np.nan,
            "mean_shared_genes": np.nan,
            "max_shared_genes": np.nan,
            "n_overlap_pairs": 0,
            "redundancy_warning": False
        }

    required = {"bp1", "bp2", "jaccard", "overlap_coefficient", "n_shared_genes"}
    if not required.issubset(set(gene_overlap_df.columns)):
        return {
            "mean_jaccard": np.nan,
            "max_jaccard": np.nan,
            "mean_overlap_coefficient": np.nan,
            "max_overlap_coefficient": np.nan,
            "mean_shared_genes": np.nan,
            "max_shared_genes": np.nan,
            "n_overlap_pairs": 0,
            "redundancy_warning": False
        }

    sub = gene_overlap_df[
        gene_overlap_df["bp1"].astype(str).isin(terms) &
        gene_overlap_df["bp2"].astype(str).isin(terms)
    ].copy()

    if sub.empty:
        return {
            "mean_jaccard": 0.0,
            "max_jaccard": 0.0,
            "mean_overlap_coefficient": 0.0,
            "max_overlap_coefficient": 0.0,
            "mean_shared_genes": 0.0,
            "max_shared_genes": 0.0,
            "n_overlap_pairs": 0,
            "redundancy_warning": False
        }

    mean_j = float(sub["jaccard"].mean())
    max_j = float(sub["jaccard"].max())
    mean_oc = float(sub["overlap_coefficient"].mean())
    max_oc = float(sub["overlap_coefficient"].max())
    mean_shared = float(sub["n_shared_genes"].mean())
    max_shared = float(sub["n_shared_genes"].max())

    warn = (
        mean_j >= MEAN_JACCARD_WARNING or
        max_j >= MAX_JACCARD_WARNING or
        mean_oc >= MEAN_OVERLAP_COEF_WARNING
    )

    return {
        "mean_jaccard": mean_j,
        "max_jaccard": max_j,
        "mean_overlap_coefficient": mean_oc,
        "max_overlap_coefficient": max_oc,
        "mean_shared_genes": mean_shared,
        "max_shared_genes": max_shared,
        "n_overlap_pairs": int(sub.shape[0]),
        "redundancy_warning": bool(warn)
    }


def infer_module_function_label(terms):
    """
    Lightweight keyword-based functional label.
    This is only an automatic draft label.
    """
    text = " ".join([str(t).upper() for t in terms])

    buckets = [
        ("ECM_EMT_INVASION", ["COLLAGEN", "EXTRACELLULAR", "MATRIX", "ECM", "ADHESION", "MIGRATION", "INVAS", "MOTILITY", "CARTILAGE"]),
        ("IMMUNE_INFLAMMATORY", ["INTERFERON", "CYTOKINE", "INFLAM", "T CELL", "B CELL", "LYMPH", "ANTIGEN", "IMMUNE", "LEUKOCYTE", "MACROPHAGE", "CHEMOKINE"]),
        ("PROLIFERATION_CELL_CYCLE", ["MITOTIC", "MITOSIS", "CELL CYCLE", "CHROMOSOME", "DNA REPLICATION", "E2F", "G2M", "SPINDLE"]),
        ("DNA_DAMAGE_REPAIR", ["DNA REPAIR", "DAMAGE", "P53", "RECOMBINATION", "DOUBLE STRAND", "NUCLEOTIDE EXCISION"]),
        ("METABOLISM", ["METABOL", "CATABOL", "BIOSYNTH", "GLYCOL", "LIPID", "FATTY", "AMINO ACID", "TRYPTOPHAN", "NAD", "SULFUR", "FOLIC"]),
        ("SIGNALING_TRANSPORT_MEMBRANE", ["SIGNALING", "TRANSPORT", "ENDOCYTOSIS", "VESICLE", "MEMBRANE", "RAFT", "RECEPTOR", "ION"]),
        ("TRANSLATION_RNA_PROCESSING", ["TRANSLATION", "MRNA", "RNA", "RIBOSOME", "SPLICING"]),
        ("STRESS_HYPOXIA_APOPTOSIS", ["STRESS", "HYPOXIA", "APOPTOSIS", "DEATH", "UV", "OXIDATIVE"]),
        ("DEVELOPMENT_DIFFERENTIATION", ["DEVELOPMENT", "DIFFERENTIATION", "MORPHOGENESIS", "ORGANOGENESIS"]),
    ]

    scores = {}
    for label, kws in buckets:
        scores[label] = sum(text.count(k) for k in kws)

    best_label, best_score = max(scores.items(), key=lambda x: x[1])
    if best_score == 0:
        return "MIXED_OR_UNLABELED"

    # If multiple labels tied/high, mark mixed
    sorted_scores = sorted(scores.items(), key=lambda x: x[1], reverse=True)
    if len(sorted_scores) > 1 and sorted_scores[1][1] > 0 and sorted_scores[1][1] >= 0.7 * best_score:
        return f"MIXED_{best_label}_{sorted_scores[1][0]}"

    return best_label


def compute_module_summary(cancer_code, group_name, G, module_assignment_df, gene_overlap_df):
    """
    Module-level summary.
    """
    rows = []

    if module_assignment_df.empty:
        return pd.DataFrame()

    for module_id, sub in module_assignment_df.groupby("module_id"):
        terms = sub["term"].astype(str).tolist()
        hub_row = sub.sort_values(
            ["degree", "weighted_degree", "D_stage"],
            ascending=False
        ).iloc[0]

        edge_stats = module_internal_edge_stats(G, terms)
        overlap_stats = compute_module_gene_overlap_stats(terms, gene_overlap_df)

        trend_counts = sub["stage_trend"].value_counts(dropna=False).to_dict()
        n_late_high = int(trend_counts.get("late_high", 0))
        n_late_low = int(trend_counts.get("late_low", 0))
        n_terms = int(sub.shape[0])

        mean_delta = float(sub["delta_late_minus_early"].mean()) if "delta_late_minus_early" in sub.columns else np.nan
        mean_abs_delta = float(sub["delta_late_minus_early"].abs().mean()) if "delta_late_minus_early" in sub.columns else np.nan
        mean_D = float(sub["D_stage"].mean()) if "D_stage" in sub.columns else np.nan
        max_D = float(sub["D_stage"].max()) if "D_stage" in sub.columns else np.nan

        functional_label = infer_module_function_label(terms)

        rows.append({
            "cancer": cancer_code,
            "group": group_name,
            "module_id": module_id,
            "functional_label_auto": functional_label,
            "n_BP": n_terms,
            "hub_BP": hub_row["term"],
            "hub_degree": safe_float(hub_row.get("degree", np.nan)),
            "hub_weighted_degree": safe_float(hub_row.get("weighted_degree", np.nan)),
            "hub_D_stage": safe_float(hub_row.get("D_stage", np.nan)),
            "mean_D_stage": mean_D,
            "max_D_stage": max_D,
            "mean_delta_late_minus_early": mean_delta,
            "mean_abs_delta_late_minus_early": mean_abs_delta,
            "n_late_high": n_late_high,
            "n_late_low": n_late_low,
            "late_high_fraction": n_late_high / n_terms if n_terms > 0 else np.nan,
            "late_low_fraction": n_late_low / n_terms if n_terms > 0 else np.nan,
            "top_5_BP": " | ".join(sub.sort_values("D_stage", ascending=False)["term"].astype(str).head(5).tolist()),
            **edge_stats,
            **overlap_stats
        })

    out = pd.DataFrame(rows)

    if not out.empty:
        out = out.sort_values(
            ["group", "n_BP", "mean_D_stage", "module_density"],
            ascending=[True, False, False, False]
        )

    return out


